#Tipos int e float
# int -> numero inteiro
# O tipo int representa qualquer numero
#positivo ou negativo, sem casas decimais.
#int sem sinal é considerado positivo
print(11) #int
print(-11) #int
print(0) #int

#float -> numero real
#O tipo float representa qualquer numero
#positivo ou negativo, com casas decimais (pontos flutuantes).
#float sem sinal é considerado positivo
#print(1.1, 10.11) #float 

# A função type mostra o tipo que o python inferiu ao valor
print( type('otavio') )
print( type(1) )
print( type(-1) )
print( type(0) )
print( type(1.1), type(-1.1), type(0.0) )